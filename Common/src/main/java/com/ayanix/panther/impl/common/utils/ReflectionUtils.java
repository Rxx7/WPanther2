public class ReflectionUtils
{

	public static boolean doesClassExist(String clazz)
	{
		try
		{
			Class.forName(clazz);
		} catch (Exception e)
		{
			return false;
		}

		return true;
	}

	public static boolean doesMethodExist(String clazz, String methodName, Class<?>... types)
	{
		try
		{
			Class.forName(clazz).getMethod(methodName, types);
		} catch (Exception e)
		{
			return false;
		}

		return true;
	}

}
