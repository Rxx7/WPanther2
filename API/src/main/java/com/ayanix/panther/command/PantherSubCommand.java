package com.ayanix.panther.command;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Panther - Developed by Lewes D. B.
 * All rights reserved 2017.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface PantherSubCommand
{

	/**
	 * @return Aliases of subcommands.
	 */
	String[] aliases();

	/**
	 * @return Minimum number of arguments required.
	 */
	int minArgs() default 0;

	/**
	 * @return Permission required to run.
	 */
	String permission() default "";

	/**
	 * @return If true, only player can execute.
	 */
	boolean onlyPlayer() default false;

}